//Operadores de atribuição...



const a = 7
let b = 3

// Acrecentando o valor de a para b...
b += a // b = b + a...
console.log(b)


// Subraindo 4 do valor de b...
b -= 4 // b = b - 4
console.log(b)

// Multiplicando o valor de b por 2...
b *= 2 // b = b * 2
console.log(b)

// Dividindo b por 2...
b /= 2 // b = b / 2
console.log(b)

// Aqui ele calcula o resto da divisão, retorn 1 ou 0...
b %= 2 // b = b % 2
console.log(b)